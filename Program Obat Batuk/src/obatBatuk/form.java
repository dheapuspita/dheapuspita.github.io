    public void TampilData() {
        DftTblModel_obat = new DefaultTableModel();
        DftTblModel_obat.addColumn("KODE");
        DftTblModel_obat.addColumn("NAMA OBAT");
        DftTblModel_obat.addColumn("KATEGORI");
        DftTblModel_obat.addColumn("JENIS");
        DftTblModel_obat.addColumn("STOK");
        DftTblModel_obat.addColumn("HARGA");
        tabel.setModel(DftTblModel_obat);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from tbobat";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_obat.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("nama"),
                    res.getString("kategori"),
                    res.getString("jenis"),
                    res.getString("stok"),
                    res.getString("harga")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
