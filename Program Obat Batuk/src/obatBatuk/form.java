    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed

        try {
            int pil=0;
            String kategori = null;
            String jenis = "";
            if (rbTablet.isSelected()){
                kategori="Tablet";
   
            }
            else if (rbSirup.isSelected()){
                kategori="Sirup";
            }
            pil= cbJenis.getSelectedIndex();
            if (pil==0) { jenis = "";}
            if (pil==1) { jenis = "Batuk Kering";}
            if (pil==2) { jenis = "Batuk Berdahak";}
            if (pil==3) { jenis = "Batuk Paroksimal";}
            if (pil==4) { jenis = "Batuk Croup";}
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("insert into tbobat(kode, nama, kategori, jenis, stok, harga) values(?,?,?,?,?,?)");
            stmt.setString(1, tfKode.getText());
            stmt.setString(2, tfNama.getText());
            stmt.setString(3, kategori);
            stmt.setString(4, jenis);
            stmt.setString(5, tfStok.getText());
            stmt.setString(6, tfHarga.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            Bersih();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }   
    }//GEN-LAST:event_btnTambahActionPerformed

